        private void loadMapData() {
                DefaultTableModel model = (DefaultTableModel) map_table.getModel();
                model.setRowCount(0); // Clear existing data

                try {
                        // Replace with your actual database connection details
                        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/timesync",
                                        "root", "aswin@2004");
                        Statement stmt = con.createStatement();
                        String query = "SELECT * FROM map"; // Modify according to your table
                        ResultSet rs = stmt.executeQuery(query);

                        while (rs.next()) {
                                String class_name = rs.getString("class");
                                String subject = rs.getString("subject");
                                String faculty = rs.getString("faculty");
                                String hours = String.valueOf(rs.getInt("hours"));
                                model.addRow(new Object[] { class_name, subject, faculty, hours });
                        }

                        rs.close();
                        stmt.close();
                        con.close();
                } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage(), "Error",
                                        JOptionPane.ERROR_MESSAGE);
                }
        }

