        private void map_btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {
                int selectedRow = map_table.getSelectedRow();

                if (selectedRow == -1) {
                        JOptionPane.showMessageDialog(this, "Please select a Map to delete!", "Warning",
                                        JOptionPane.WARNING_MESSAGE);
                        return;
                }

                DefaultTableModel model = (DefaultTableModel) map_table.getModel();
                String classname = model.getValueAt(selectedRow, 0).toString().trim();
                String subject = model.getValueAt(selectedRow, 1).toString().trim();
                String faculty = model.getValueAt(selectedRow, 2).toString().trim();
                String hours = model.getValueAt(selectedRow, 3).toString().trim();

                System.out.println("Deleting: ");
                System.out.println("Class: " + classname);
                System.out.println("Subject: " + subject);
                System.out.println("Faculty: " + faculty);
                System.out.println("Hours: " + hours);

                int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this subject?",
                                "Confirm Deletion", JOptionPane.YES_NO_OPTION);

                if (confirm == JOptionPane.YES_OPTION) {
                        try {
                                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/timesync",
                                                "root", "aswin@2004");

                                String query = "DELETE FROM map WHERE class = ? and subject = ? and faculty = ? and hours = ?";
                                PreparedStatement pstmt = con.prepareStatement(query);
                                pstmt.setString(1, classname);
                                pstmt.setString(2, subject);
                                pstmt.setString(3, faculty);
                                pstmt.setString(4, hours);

                                int rowsAffected = pstmt.executeUpdate();
                                pstmt.close();
                                con.close();

                                if (rowsAffected > 0) {
                                        model.removeRow(selectedRow); // Remove from table
                                        JOptionPane.showMessageDialog(this, "Map deleted successfully!", "Success",
                                                        JOptionPane.INFORMATION_MESSAGE);
                                } else {
                                        JOptionPane.showMessageDialog(this, "Map not found in database!", "Error",
                                                        JOptionPane.ERROR_MESSAGE);
                                }
                        } catch (Exception e) {
                                e.printStackTrace();
                                JOptionPane.showMessageDialog(this, "Error deleting class: " + e.getMessage(), "Error",
                                                JOptionPane.ERROR_MESSAGE);
                        }
                }
        }

        private void map_btn_mapActionPerformed(java.awt.event.ActionEvent evt) {
                String class_name = (String) map_cmp_class.getSelectedItem();
                String subject_name = (String) map_cmp_subject.getSelectedItem();
                String faculty_name = (String) map_cmp_faculty.getSelectedItem();
                String hours = (String) map_tf_hours.getText();

                try {
                        // Database Connection
                        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/timesync",
                                        "root", "aswin@2004");
                        String sql = "INSERT INTO map (class,subject,faculty,hours) VALUES (?, ?,?,?)";
                        PreparedStatement pst = con.prepareStatement(sql);
                        pst.setString(1, class_name);
                        pst.setString(2, subject_name);
                        pst.setString(3, faculty_name);
                        pst.setString(4, hours);

                        int rowsInserted = pst.executeUpdate();
                        if (rowsInserted > 0) {
                                JOptionPane.showMessageDialog(this, "Class added successfully!");

                                // Adding to table model
                                DefaultTableModel model = (DefaultTableModel) map_table.getModel();
                                model.addRow(new Object[] { class_name, subject_name, faculty_name, hours });
                        }

                        // Close resources
                        pst.close();
                        con.close();
                } catch (SQLException e) {
                        JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Error",
                                        JOptionPane.ERROR_MESSAGE);
                }
        }

