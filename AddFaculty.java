        private void faculty_btn_deletefacultyActionPerformed(java.awt.event.ActionEvent evt) {
                int selectedRow = faculty_table.getSelectedRow(); // Get selected row index

                if (selectedRow == -1) {
                        JOptionPane.showMessageDialog(this, "Please select a class to delete!", "Warning",
                                        JOptionPane.WARNING_MESSAGE);
                        return;
                }

                DefaultTableModel model = (DefaultTableModel) faculty_table.getModel();
                String classCode = model.getValueAt(selectedRow, 1).toString(); // Assuming class_code is in column
                                                                                // index 1

                int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this class?",
                                "Confirm Deletion", JOptionPane.YES_NO_OPTION);

                if (confirm == JOptionPane.YES_OPTION) {
                        try {
                                // Replace with your actual database connection details
                                Connection con = DriverManager.getConnection(
                                                "jdbc:mysql://localhost:3306/timesync", "root", "aswin@2004");
                                String query = "DELETE FROM faculty WHERE faculty_code = ?"; // Modify as needed
                                PreparedStatement pstmt = con.prepareStatement(query);
                                pstmt.setString(1, classCode);

                                int rowsAffected = pstmt.executeUpdate();
                                pstmt.close();
                                con.close();

                                if (rowsAffected > 0) {
                                        model.removeRow(selectedRow); // Remove row from table
                                        JOptionPane.showMessageDialog(this, "Class deleted successfully!", "Success",
                                                        JOptionPane.INFORMATION_MESSAGE);
                                } else {
                                        JOptionPane.showMessageDialog(this, "Class not found in database!", "Error",
                                                        JOptionPane.ERROR_MESSAGE);
                                }

                        } catch (Exception e) {
                                JOptionPane.showMessageDialog(this, "Error deleting class: " + e.getMessage(), "Error",
                                                JOptionPane.ERROR_MESSAGE);
                        }
                }
        }

        private void LoadFacultyTable() {
                DefaultTableModel model = (DefaultTableModel) faculty_table.getModel();
                model.setRowCount(0); // Clear existing data

                try {
                        // Replace with your actual database connection details
                        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/timesync",
                                        "root", "aswin@2004");
                        Statement stmt = con.createStatement();
                        String query = "SELECT faculty_name, faculty_code FROM faculty"; // Modify according to your
                                                                                         // table
                        ResultSet rs = stmt.executeQuery(query);

                        while (rs.next()) {
                                String className = rs.getString("faculty_name");
                                String classCode = rs.getString("faculty_code");
                                model.addRow(new Object[] { className, classCode });
                        }

                        rs.close();
                        stmt.close();
                        con.close();
                } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage(), "Error",
                                        JOptionPane.ERROR_MESSAGE);
                }
        }

