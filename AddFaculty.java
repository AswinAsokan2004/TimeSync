        private void faculty_addfacultyActionPerformed(java.awt.event.ActionEvent evt) {
                String faculty_name = faculty_tf_facultyname.getText();
                String faculty_code = faculty_tf_facultycode.getText();

                if (faculty_name.isEmpty() || faculty_code.isEmpty()) {
                        JOptionPane.showMessageDialog(this, "Please fill all the fields", "Try Again",
                                        JOptionPane.ERROR_MESSAGE);
                } else {
                        try {
                                // Database Connection
                                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/timesync",
                                                "root", "aswin@2004");
                                String sql = "INSERT INTO faculty (faculty_name, faculty_code) VALUES (?, ?)";
                                PreparedStatement pst = con.prepareStatement(sql);
                                pst.setString(1, faculty_name);
                                pst.setString(2, faculty_code);

                                int rowsInserted = pst.executeUpdate();
                                if (rowsInserted > 0) {
                                        JOptionPane.showMessageDialog(this, "Class added successfully!");

                                        // Adding to table model
                                        DefaultTableModel model = (DefaultTableModel) faculty_table.getModel();
                                        model.addRow(new Object[] { faculty_name, faculty_code });
                                }

                                // Close resources
                                pst.close();
                                con.close();
                        } catch (SQLException e) {
                                JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Error",
                                                JOptionPane.ERROR_MESSAGE);
                        }
                }

                faculty_tf_facultyname.setText("");
                faculty_tf_facultycode.setText("");
        }

